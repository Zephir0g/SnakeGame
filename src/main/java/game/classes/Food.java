package game.classes;

import java.util.Random;

public class Food {

    private int x;
    private int y;

    public Food(int x, int y) {
        super();
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }

}
